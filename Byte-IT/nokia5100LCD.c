void LcdString(char *characters)
{
    while (*characters)
    {
        LcdCharacter(*characters++);
    }
}

void LcdCharacter(char character)
{
    LcdWrite(0x00);
    for (int index = 0; index < 5; index++)
    {
        LcdWrite(ASCII[character - 0x20][index]);
    }
    LcdWrite(0x00);
}

unsigned char LCDBuffer[84][6];

void LcdInitBuffer() {
	for (unsigned char x = 0; x < 84; ++x) {
		for (unsigned char y = 0; y < 6; ++y)
			LCDBuffer[x][y] = 0x00;
	}
}


void LcdClear(void)
{
	for (long long index = 0; index < 84*48; index++)
	{
		LcdWrite(0x00);
	}
}

void LcdWrite(unsigned char data)
{
	DATAPORT |= PIN_DC; //digitalWrite(PIN_DC, dc);
	DATAPORT &= NOT_PIN_SCE; //digitalWrite(PIN_SCE, LOW);
	shiftBits(data);
	DATAPORT |= PIN_SCE; //digitalWrite(PIN_SCE, HIGH);
}

void LcdWriteInit(unsigned char data)
{
	DATAPORT &= NOT_PIN_DC; //digitalWrite(PIN_DC, dc);
	DATAPORT &= NOT_PIN_SCE; //digitalWrite(PIN_SCE, LOW);
	shiftBits(data);
	DATAPORT |= PIN_SCE; //digitalWrite(PIN_SCE, HIGH);
}

void LcdInitialise(void)
{
	DATAPORT &= NOT_PIN_RESET; //digitalWrite(PIN_RESET, LOW);
	DATAPORT |= PIN_RESET; //digitalWrite(PIN_RESET, HIGH);
	LcdWriteInit(0x21);  // LCD Extended Commands.
	LcdWriteInit(0xB1);  // Set LCD Vop (Contrast).
	LcdWriteInit(0x04);  // Set Temp coefficent. //0x04
	LcdWriteInit(0x14);  // LCD bias mode 1:48. //0x13
	LcdWriteInit(0x20);  // LCD Basic Commands
	LcdWriteInit(0x0C);  // LCD in normal mode.

	LcdInitBuffer();
	LcdClear();
}

void gotoXY(unsigned char x, unsigned char y) {
	LcdWriteInit(0x80 | x);  // Column.
	LcdWriteInit(0x40 | y);  // Row.
}

void updatePixel(int col, int row, unsigned char fill)
{
    int hPos = row/8;
	int pos = row%8;

	gotoXY(col, hPos);

	if(fill == 1) //fill
	{
		if (pos == 0) 
			LCDBuffer[col][hPos] = LCDBuffer[col][hPos] | 0x01;
		else if (pos == 1)
			LCDBuffer[col][hPos] = LCDBuffer[col][hPos] | 0x02;
		else if (pos == 2) 
			LCDBuffer[col][hPos] = LCDBuffer[col][hPos] | 0x04;
		else if (pos == 3)
			LCDBuffer[col][hPos] = LCDBuffer[col][hPos] | 0x08;
		else if (pos == 4)
			LCDBuffer[col][hPos] = LCDBuffer[col][hPos] | 0x10;
		else if (pos == 5)
			LCDBuffer[col][hPos] = LCDBuffer[col][hPos] | 0x20;
		else if (pos == 6)
			LCDBuffer[col][hPos] = LCDBuffer[col][hPos] | 0x40;
		else if (pos == 7)
			LCDBuffer[col][hPos] = LCDBuffer[col][hPos] | 0x80;
	}
	else //erase
	{
		if (pos == 0) 
			LCDBuffer[col][hPos] = LCDBuffer[col][hPos] & ~0x01;
		else if (pos == 1)
			LCDBuffer[col][hPos] = LCDBuffer[col][hPos] & ~0x02;
		else if (pos == 2) 
			LCDBuffer[col][hPos] = LCDBuffer[col][hPos] & ~0x04;
		else if (pos == 3)
			LCDBuffer[col][hPos] = LCDBuffer[col][hPos] & ~0x08;
		else if (pos == 4)
			LCDBuffer[col][hPos] = LCDBuffer[col][hPos] & ~0x10;
		else if (pos == 5)
			LCDBuffer[col][hPos] = LCDBuffer[col][hPos] & ~0x20;
		else if (pos == 6)
			LCDBuffer[col][hPos] = LCDBuffer[col][hPos] & ~0x40;
        else if (pos == 7)
            LCDBuffer[col][hPos] = LCDBuffer[col][hPos] & ~0x80;
	}

    LcdWrite(LCDBuffer[col][row/8]);
    gotoXY(99, 99); //move cursor out of bound
    LcdWrite(0x00);

}
